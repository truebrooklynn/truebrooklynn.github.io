const root = document.getElementById('galleryRoot');
let defaultFilter = "none";

function filterSelection(selectedFilter){
  // clear the page
  root.replaceChildren();

  for (let i = 0; i < artPortfolioImages.length; i++) {
    if ((selectedFilter === "none") || (artPortfolioImages[i].tagMedium === selectedFilter)) {
      var imageDiv = document.createElement('div');
      imageDiv.classList.add('oneImage');
    
      // Render image heading
      var mainHeading = document.createElement('h1');
      mainHeading.appendChild(document.createTextNode(artPortfolioImages[i].title));
      imageDiv.appendChild(mainHeading);
    
      // Render image
      var singleImage = document.createElement('img');
      singleImage.src = artPortfolioImages[i].link;
      imageDiv.appendChild(singleImage);
    
      // Render description
      var description = document.createElement('p');
      description.appendChild(document.createTextNode(artPortfolioImages[i].description));
      imageDiv.appendChild(description);
      
      root.appendChild(imageDiv);
    }  
  };
}

filterSelection(defaultFilter)

// Add active class to the current button (highlight it)
var btnContainer = document.getElementById("galleryFilters");
var filterButtons = document.getElementById("galleryFilters").getElementsByClassName("filterButton");
for (var i = 0; i < filterButtons.length; i++) {
  filterButtons[i].addEventListener("click", function(){
    var current = document.getElementsByClassName("active");
    current[0].className = current[0].className.replace(" active", "");
    this.className += " active";
  });
}
